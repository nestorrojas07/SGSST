<h1>Acerca de </h1>

<p><strong>Ley que rige el SGSST?</strong><BR>
De conformidad con el artículo 2° del Decreto 1295 de 1994; la Comunidad Andina, de la cual Colombia es país miembro, en la Decisión 584 adoptó el "Instrumento Andino de Seguridad y Salud en el Trabajo", Que el artículo 56 del Decreto 1295 de 1994, Que el artículo 1° de la Ley 1562 del 11 de julio de 2012; Que la Organización Internacional del Trabajo - OIT publicó en el año 2001, las Directrices relativas a los sistemas de gestión de la 'seguridad y la salud en el trabajo (ILO-OSH 2001) la cual fue tomada como guía para realizar el presente decreto.<BR><BR>


<strong>Quien debe implementar el SGSST?</strong><BR>
El SG-SST, debe ser aplicado por todos los empleadores públicos y privados, los contratantes de personal bajo modalidad de contrato civil, comercial o administrativo, las organizaciones de economía solidaria y del sector cooperativo, las empresas de servicios temporales y tener cobertura sobre los trabajadores dependientes, contratistas, trabajadores cooperados y los trabajadores en misión.<BR><BR>

<strong>Objetivo del SST?</strong><BR>
Tiene por objeto mejorar las condiciones y el medio ambiente de trabajo, así como la salud en el trabajo,que conlleva la promoción y el mantenimiento del bienestar físico, mental y social de los trabajadores en todas las ocupaciones.<BR><BR>

<strong>Objetivo del SGSST?</strong><BR>
SG-SST consiste en el desarrollo de un proceso lógico y por etapas, basado en la mejora, continua y que incluye la política, la organización, la' planificación, la aplicación,la evaluación, la auditoría y las acciones de mejora con el objetivo de anticipar, reconocer, evaluar y controlar los riesgos que puedan afectar la seguridad y la salud en el trabajo; el cual debe ser liderado e implementado por el empleador o contratante.<BR><BR>

El empleador o contratante debe establecer por escrito una política de Seguridad y Salud en el Trabajo - SST que debe ser parte de las políticas de gestión de la empresa, con alcance sobre todos, sus centros de trabajo y todos sus trabajadores, independiente de su forma de, contratación o vinculación, incluyendo los contratistas y subcontratistas.<BR><BR>


<strong>Requisitos de la política SST.</strong><BR>
1. Establecer el compromiso de la empresa hacia la implementación del SST de la empresa para la gestión de los riesgos laborales;<BR><BR>

2. Ser específica para la empresa y apropiada para la naturaleza de sus peligros y el tamaño de la organización;<BR><BR>

3. Ser concisa, redactada con claridad, estar fechada y firmada por el representante legal de la empresa; .<BR><BR>

4. Debe ser difundida a todos los niveles de la organización y estar accesible. a todos los trabajadores y demás partes interesadas, en el lugar de trabajo y ser revisada como mínimo una vez al año y de requerirse, actualizada acorde con los cambios tanto en materia de Seguridad y Salud en el Trabajo - SST, como en la empresa.<BR><BR>

<strong>Objetivos de la política SST.</strong><BR>
1. Identificar los peligros, evaluar y valorar los riesgos y establecer los respectivos controles;<BR><BR>

2. Proteger la seguridad y salud de todos los trabajadores, mediante la mejora continua del Sistema de Gestión de la Seguridad y Salud en el Trabajo SG-SST en la empresa ycumplir la normatividad nacional vigente aplicable en materia de riesgos laborales.</p>


